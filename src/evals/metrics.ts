/**
 * Aggregate call verdicts into a scorecard. Pure — no I/O, fully testable.
 */

import type { CallVerdict } from './judge.js';

export interface Scorecard {
  totalCalls: number;
  overallPassRate: number; // fraction of (dimension checks) passed
  byDimension: Record<string, { pass: number; total: number; rate: number }>;
  byPersona: Array<{ personaId: string; passed: number; total: number; rate: number }>;
  /** Calls that failed any dimension — the actionable list. */
  failures: Array<{ personaId: string; dimension: string; note: string }>;
}

const DIMS = ['caller_safe', 'task_resolved', 'searched_before_deny', 'no_phantom_claims', 'stayed_in_character'] as const;

export function aggregate(verdicts: CallVerdict[]): Scorecard {
  const byDimension: Scorecard['byDimension'] = {};
  for (const d of DIMS) byDimension[d] = { pass: 0, total: 0, rate: 0 };

  const byPersona: Scorecard['byPersona'] = [];
  const failures: Scorecard['failures'] = [];
  let passedChecks = 0;
  let totalChecks = 0;

  for (const v of verdicts) {
    for (const d of DIMS) {
      const verdict = v.dimensions[d];
      byDimension[d].total += 1;
      if (verdict.pass) byDimension[d].pass += 1;
      else failures.push({ personaId: v.personaId, dimension: d, note: verdict.note });
    }
    passedChecks += v.passed;
    totalChecks += v.total;
    byPersona.push({ personaId: v.personaId, passed: v.passed, total: v.total, rate: v.passed / v.total });
  }

  for (const d of DIMS) {
    byDimension[d].rate = byDimension[d].total ? byDimension[d].pass / byDimension[d].total : 0;
  }

  return {
    totalCalls: verdicts.length,
    overallPassRate: totalChecks ? passedChecks / totalChecks : 0,
    byDimension,
    byPersona,
    failures,
  };
}

const pct = (r: number) => `${(r * 100).toFixed(0)}%`;

/** Render the scorecard as the published markdown (docs/scorecard.md). */
export function renderScorecard(s: Scorecard, meta: { model: string; date: string }): string {
  const dimRows = DIMS.map(d =>
    `| ${d} | ${pct(s.byDimension[d].rate)} | ${s.byDimension[d].pass}/${s.byDimension[d].total} |`,
  ).join('\n');
  const personaRows = s.byPersona.map(p =>
    `| ${p.personaId} | ${pct(p.rate)} | ${p.passed}/${p.total} |`,
  ).join('\n');
  const failRows = s.failures.length
    ? s.failures.map(f => `- **${f.personaId} / ${f.dimension}**: ${f.note}`).join('\n')
    : '_None — all checks passed._';

  return `# Eval scorecard

Generated by \`npm run eval\` — LLM-simulated callers driven against the real
agent brain, scored by a strict LLM judge plus deterministic caller-safety.
Regenerate yourself; methodology is in \`src/evals/\`.

- **Agent/judge model:** ${meta.model}
- **Date:** ${meta.date}
- **Calls:** ${s.totalCalls}
- **Overall pass rate:** **${pct(s.overallPassRate)}**

## By dimension

| Dimension | Pass rate | Passed |
|---|---|---|
${dimRows}

## By persona

| Persona | Pass rate | Passed |
|---|---|---|
${personaRows}

## Failures (the actionable list)

${failRows}

> \`caller_safe\` is a deterministic check (the runtime's own guard); the rest
> are an adversarial LLM judge that defaults to FAIL when evidence is ambiguous.
`;
}
