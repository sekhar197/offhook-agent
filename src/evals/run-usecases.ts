/**
 * `npm run eval:usecases` — run the eval against every shipped use-case config
 * and write a coverage report (docs/usecases.md) plus the console table.
 *
 * Model selection:
 *  - Default: each config uses its own models.llm (the honest per-use-case test).
 *  - Override all to one capable model for comparable scores:
 *      OFFHOOK_AGENT_EVAL_PROVIDER=openai OFFHOOK_AGENT_EVAL_MODEL=gpt-5.4-mini
 *  - A capable judge matters; it follows the same override.
 */

import { writeFileSync, mkdirSync } from 'node:fs';
import { join, dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { loadAgentConfig, toAgentIdentity, llmConfigInput } from '../config/agent-config.js';
import { loadKnowledgeFolder } from '../knowledge/loader.js';
import { hybridSearch } from '../search/hybrid-search.js';
import { ToolRegistry, type ToolContext } from '../tools/registry.js';
import { BUILTIN_TOOLS } from '../tools/builtins.js';
import { resolveLlm, type LlmProviderName } from '../llm/provider.js';
import { createLlmClient, type LlmClient } from '../llm/client.js';
import { EMPTY_VOCABULARY, type SearchVocabulary } from '../types.js';
import { simulateCall } from './simulate.js';
import { judgeCall } from './judge.js';
import { aggregate, type Scorecard } from './metrics.js';
import { USE_CASES, type UseCase } from './usecases.js';

/** A single capable model for the agent+judge, if overridden via env. */
function overrideClient(): LlmClient | null {
  const provider = process.env.OFFHOOK_AGENT_EVAL_PROVIDER as LlmProviderName | undefined;
  const model = process.env.OFFHOOK_AGENT_EVAL_MODEL;
  if (!provider || !model) return null;
  const llm = resolveLlm({ provider, model, maxTokens: 200,
    ...(process.env.OFFHOOK_AGENT_EVAL_BASEURL ? { baseUrl: process.env.OFFHOOK_AGENT_EVAL_BASEURL } : {}) });
  return createLlmClient(llm);
}

async function runUseCase(uc: UseCase, override: LlmClient | null): Promise<{ uc: UseCase; scorecard: Scorecard; model: string }> {
  const config = loadAgentConfig(uc.config);
  const identity = toAgentIdentity(config);
  const entries = loadKnowledgeFolder(resolve(dirname(resolve(uc.config)), config.knowledge.folder));
  const vocabulary: SearchVocabulary = {
    ...EMPTY_VOCABULARY,
    categorySynonyms: config.knowledge.vocabulary.categorySynonyms,
    aliases: config.knowledge.vocabulary.aliases,
  };

  const { client, llm } = override ?? createLlmClient(resolveLlm(llmConfigInput(config)));

  const registry = new ToolRegistry();
  for (const t of BUILTIN_TOOLS) registry.register(t);
  const toolContext: ToolContext = {
    callId: 'eval', correlationId: uc.id, agentId: config.agent.id, state: {},
    searchKnowledge: async (q) => (await hybridSearch(q, entries, [], { vocabulary }))
      .map(r => ({ id: r.item.id, name: r.item.name, category: r.item.category, ...(r.item.description ? { description: r.item.description } : {}) })),
    executeAction: async () => ({ status: 'ok' }),
    transferToHuman: async () => {},
    endCall: async () => {},
  };

  const verdicts = [];
  for (const persona of uc.personas) {
    const call = await simulateCall({
      persona,
      personaClient: client, personaLlm: llm,
      agentClient: client, agentLlm: llm,
      registry, enabledTools: config.tools.enabled, toolContext,
      promptContext: { identity, entries },
    });
    verdicts.push(await judgeCall(call, client, llm));
  }
  return { uc, scorecard: aggregate(verdicts), model: `${llm.provider}/${llm.model}` };
}

function renderReport(results: Array<{ uc: UseCase; scorecard: Scorecard; model: string }>, date: string): string {
  const rows = results.map(r =>
    `| ${r.uc.name} | ${r.model} | **${(r.scorecard.overallPassRate * 100).toFixed(0)}%** | ${r.scorecard.totalCalls} |`,
  ).join('\n');
  return `# Use-case coverage

Generated by \`npm run eval:usecases\` — every shipped example config run through
simulated callers (in-language for multilingual), scored by the eval judge.
Each row is a reproducible test of an advertised use case, not a claim.

- **Date:** ${date}

| Use case | Model | Pass rate | Calls |
|---|---|---|---|
${rows}

> Multilingual cases run callers that speak the target language. \`caller_safe\`
> is deterministic; the rest are an adversarial LLM judge. Regenerate with a
> capable model (OFFHOOK_AGENT_EVAL_PROVIDER/MODEL) for comparable, publishable scores.
`;
}

async function main() {
  const override = overrideClient();
  const only = process.env.OFFHOOK_AGENT_EVAL_ONLY?.split(',');
  const cases = only ? USE_CASES.filter(u => only.includes(u.id)) : USE_CASES;

  console.log(`Testing ${cases.length} use cases${override ? ` on ${override.llm.provider}/${override.llm.model}` : ' (each on its own model)'}...\n`);
  const results = [];
  for (const uc of cases) {
    process.stdout.write(`  ${uc.id}... `);
    try {
      const r = await runUseCase(uc, override);
      results.push(r);
      console.log(`${(r.scorecard.overallPassRate * 100).toFixed(0)}% (${r.scorecard.totalCalls} calls)`);
    } catch (e) {
      console.log(`SKIP (${e instanceof Error ? e.message.split('\n')[0] : String(e)})`);
    }
  }

  const md = renderReport(results, new Date().toISOString().slice(0, 10));
  const root = join(dirname(fileURLToPath(import.meta.url)), '..', '..');
  mkdirSync(join(root, 'docs'), { recursive: true });
  writeFileSync(join(root, 'docs', 'usecases.md'), md);
  console.log(`\n→ docs/usecases.md`);
}

main().catch((e) => { console.error(e); process.exit(1); });
